package my.programs;

public class ArithmaticOps {
	public static void main(String[] args)
	{
		int n1 = 45;
		int n2 = 20;
		int sum = n1+n2;
		int mul = n1*n2;
		int div = n1/n2;
		int sub = n1-n2;
		System.out.println("Sum = "+sum+"\n"+
						   "Subtraction = "+sub+"\n"+
						   "Product = "+mul+"\n"+
						   "Division = "+div);
	}

}
