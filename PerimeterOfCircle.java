package my.programs;

public class PerimeterOfCircle {

	public static void main(String[] args) {
		double pie = 3.14;
		int r = 4;
		double perimeter = 2*pie*r;
		double area = pie*r*r;
		System.out.println("Area of circle = "+area+"\n"+
							"Perimeter of circle = "+perimeter);

	}

}
